    private void t_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_t_simpanActionPerformed
        // TODO add your handling code here:
        
        try{
            String sql = "INSERT INTO db_karyawan VALUES (?,?,?,?,?,?,?,?)";
        
            String a = nip.getText();
            String b = nama.getText();
            String c = tempat_lahir.getText();
            String d = df.format(tanggal_lahir.getDate());
            String e = (String) jk.getSelectedItem();
            int f = (int) usia.getValue();
            String g = (String) pangkat.getSelectedItem();
            String h = jabatan.getText();

            if(a.equals("")||b.equals("")||c.equals("")||d.isEmpty()||e.equals("--")||f==0||g.equals("")||h.equals("")){
                JOptionPane.showMessageDialog(null, "Silahkan Lengkapi Data Anda.","Terjadi Kesalahan!",JOptionPane.ERROR_MESSAGE);
                reset();
                nip.requestFocus();
            }else{
                try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, a);
                stat.setString(2, b);
                stat.setString(3, c);
                stat.setString(4, d);
                stat.setString(5, e);
                stat.setInt(6, f);
                stat.setString(7, g);
                stat.setString(8, h);
                stat.executeUpdate();

                JOptionPane.showMessageDialog(null, "Data Berhasil Tersimpan");
                reset();
                nip.requestFocus();

                }catch(Exception err){
                    JOptionPane.showMessageDialog(null, "NIP yang Anda Masukan Sudah Ada, Silahkan Coba Lagi.");
                    reset();
                    nip.requestFocus();
                }
            }
        }catch(Exception er){
            JOptionPane.showMessageDialog(null, "error di " + er);
        }
        
    }//GEN-LAST:event_t_simpanActionPerformed

