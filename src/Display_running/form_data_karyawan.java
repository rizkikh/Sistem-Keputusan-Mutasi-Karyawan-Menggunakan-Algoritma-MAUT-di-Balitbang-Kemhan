    private void table_dataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_dataMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 2){
            edit_data_karyawan f2 = new edit_data_karyawan();
        
            int bar = table_data.getSelectedRow();
            String nip = model.getValueAt(bar, 0).toString();
            String nama = model.getValueAt(bar, 1).toString();
            String jenis_kelamin = model.getValueAt(bar, 2).toString();
            String pangkat = model.getValueAt(bar, 3).toString();
            String jabatan = model.getValueAt(bar, 4).toString();

            f2.nip.setText(nip);
            f2.nama.setText(nama);
            f2.jk.setSelectedItem(jenis_kelamin);
            f2.pangkat.setSelectedItem(pangkat);
            f2.jabatan.setText(jabatan);

            String sql = "SELECT * FROM db_karyawan WHERE nip = '"+nip+"'";

            try{
                java.sql.Statement stat = conn.createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                while(hasil.next()){
                    String tempat_lahir = hasil.getString("tl");
                    Date tgl = hasil.getDate("ttl");
                    int usia = hasil.getInt("usia");
                    f2.usia.setValue(usia);
                    f2.tempat_lahir.setText(tempat_lahir);
                    f2.tanggal_lahir.setDate(tgl);
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Terjadi Kesalahan " + e);
            }

            
            f2.setVisible(true);
            f2.pack();
            f2.setDefaultCloseOperation(form_data_karyawan.DISPOSE_ON_CLOSE);
        }
        
    }//GEN-LAST:event_table_dataMouseClicked

